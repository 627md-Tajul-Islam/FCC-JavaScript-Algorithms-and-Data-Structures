// at once
var j = 5 + 5;
console.log(j);

// using let 
let d = 5;
let e = 5;
let f = d + e;
console.log(f);
// at once
let k = 5 + 5;
console.log(k);

// using const 
const g = 5;
const h = 5;
var i = g + h;
console.log(i);
// at once
const l = 5 + 5;
console.log(l);