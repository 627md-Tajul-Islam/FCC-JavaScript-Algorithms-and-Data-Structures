//Number is a data type in JavaScript which represents numeric data.

// using var
var a = 10;
var b = 5;
var c = a * b;
console.log(c);
// at once
var j = 10 * 5;
console.log(j);

// using let 
let d = 10;
let e = 5;
let f = d * e;
console.log(f);
// at once
let k = 10 * 5;
console.log(k);

// using const 
const g = 10;
const h = 5;
var i = g * h;
console.log(i);
// at once
const l = 10 * 5;
console.log(l);